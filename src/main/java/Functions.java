    public static String translate(final Map<String, String> vocabulary, final String engText) {
        StringBuilder builder = new StringBuilder();
        Arrays.stream(engText.split(" "))
                .forEach(engWord -> {
                    String ruWord = vocabulary.get(engWord);
                    if (ruWord == null) {
                        builder.append(engWord);
                    } else {
                        builder.append(ruWord);
                    }
                    builder.append(" ");
                });
        String ruText = builder.toString();
        return ruText;
    }


